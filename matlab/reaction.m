function v = reaction(y)
  global f_param;
  global f_var;

  Akt = y(f_var.Akt);
  Akt_PIP3 = y(f_var.Akt_PIP3);
  Akt_PI_P = y(f_var.Akt_PI_P);
  Akt_PI_PP = y(f_var.Akt_PI_PP);
  ERK = y(f_var.ERK);
  ERKP = y(f_var.ERKP);
  ERKPP = y(f_var.ERKPP);
  GS = y(f_var.GS);
  HRG = y(f_var.HRG);
  internalization = y(f_var.internalization);
  MEK = y(f_var.MEK);
  MEKP = y(f_var.MEKP);
  MEKPP = y(f_var.MEKPP);
  PI = y(f_var.PI);
  PI3K = y(f_var.PI3K);
  PI3K_act = y(f_var.PI3K_act);
  PIP3 = y(f_var.PIP3);
  R = y(f_var.R);
  RP = y(f_var.RP);
  R_HRG = y(f_var.R_HRG);
  R_HRG2 = y(f_var.R_HRG2);
  R_PI3K = y(f_var.R_PI3K);
  R_PI3K_act = y(f_var.R_PI3K_act);
  R_ShGS = y(f_var.R_ShGS);
  R_ShP = y(f_var.R_ShP);
  R_Shc = y(f_var.R_Shc);
  Raf = y(f_var.Raf);
  Raf_act = y(f_var.Raf_act);
  RasGDP = y(f_var.RasGDP);
  RasGTP = y(f_var.RasGTP);
  ShGS = y(f_var.ShGS);
  ShP = y(f_var.ShP);
  Shc = y(f_var.Shc);
  E = y(f_var.E);
  MKP3 = y(f_var.MKP3);
  PP2A = y(f_var.PP2A);

  v(1) = f_param.kf1*R*HRG - f_param.kr1*R_HRG;
  v(2) = f_param.kf2*R_HRG^2 - f_param.kr2*R_HRG2;
  v(3) = f_param.kf3*R_HRG2 - f_param.kr3*RP;
  v(4) = f_param.V4*RP/(f_param.K4+RP);
  v(5) = f_param.kf5*RP*Shc - f_param.kr5*R_Shc;
  v(6) = f_param.kf6*R_Shc - f_param.kr6*R_ShP;
  v(7) = f_param.kf7*R_ShP*GS - f_param.kr7*R_ShGS;
  v(8) = f_param.kf8*R_ShGS - f_param.kr8*ShGS*RP;
  v(9) = f_param.kf9*ShGS - f_param.kr9*GS*ShP;
  v(10) = f_param.V10*ShP/(f_param.K10+ShP);
  v(11) = f_param.kf11*ShGS*RasGDP/(f_param.K11+RasGDP);
  v(12) = f_param.V12*RasGTP/(f_param.K12+RasGTP);
  v(13) = f_param.kf13*RasGTP*Raf/(f_param.K13+Raf);
  v(14) = f_param.kf14*(Akt_PI_PP+E)*Raf_act/(f_param.K14+Raf_act);
  v(15) = f_param.kf15*Raf_act*MEK/(f_param.K15*(1.0+MEKP/f_param.K17)+MEK);
  v(16) = f_param.kf16*PP2A*MEKP/(f_param.K16*(1.0+MEKPP/f_param.K18+Akt_PI_P/f_param.K31+Akt_PI_PP/f_param.K33)+MEKP);
  v(17) = f_param.kf17*Raf_act*MEKP/(f_param.K17*(1.0+MEK/f_param.K15)+MEKP);
  v(18) = f_param.kf18*PP2A*MEKPP/(f_param.K18*(1.0+MEKP/f_param.K16+Akt_PI_P/f_param.K31+Akt_PI_PP/f_param.K33)+MEKPP);
  v(19) = f_param.kf19*MEKPP*ERK/(f_param.K19*(1.0+ERKP/f_param.K21)+ERK);
  v(20) = f_param.kf20*MKP3*ERKP/(f_param.K20*(1.0+ERKPP/f_param.K22)+ERKP);
  v(21) = f_param.kf21*MEKPP*ERKP/(f_param.K21*(1.0+ERK/f_param.K19)+ERKP);
  v(22) = f_param.kf22*MKP3*ERKPP/(f_param.K22*(1.0+ERKP/f_param.K20)+ERKPP);
  v(23) = f_param.kf23*RP*PI3K - f_param.kr23*R_PI3K;
  v(24) = f_param.kf24*R_PI3K - f_param.kr24*R_PI3K_act;
  v(25) = f_param.kf25*R_PI3K_act - f_param.kr25*RP*PI3K_act;
  v(26) = f_param.V26*PI3K_act/(f_param.K26+PI3K_act);
  v(27) = f_param.kf27*PI3K_act*PI/(f_param.K27+PI);
  v(28) = f_param.V28*PIP3/(f_param.K28+PIP3);
  v(29) = f_param.kf29*PIP3*Akt - f_param.kr29*Akt_PIP3;
  v(30) = f_param.V30*Akt_PIP3/(f_param.K30*(1.0+Akt_PI_P/f_param.K32)+Akt_PIP3);
  v(31) = f_param.kf31*PP2A*Akt_PI_P/(f_param.K31*(1.0+MEKP/f_param.K16+MEKPP/f_param.K18+Akt_PI_PP/f_param.K33)+Akt_PI_P);
  v(32) = f_param.V32*Akt_PI_P/(f_param.K32*(1.0+Akt_PIP3/f_param.K30)+Akt_PI_P);
  v(33) = f_param.kf33*PP2A*Akt_PI_PP/(f_param.K33*(1.0+MEKP/f_param.K16+MEKPP/f_param.K18+Akt_PI_P/f_param.K31)+Akt_PI_PP);
  v(34) = f_param.kf34*RP - f_param.kr34*internalization;

end